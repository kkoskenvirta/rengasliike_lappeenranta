// Current language
let currentLanguage = "fi";

// Function to get localized text
function getText(key, lang = currentLanguage) {
  const keys = key.split(".");
  let value = LOCALIZATION[lang];

  for (const k of keys) {
    if (value && value[k]) {
      value = value[k];
    } else {
      console.warn(`Translation key not found: ${key} for language: ${lang}`);
      return key;
    }
  }

  return value;
}

// Function to change language
function changeLanguage(lang) {
  currentLanguage = lang;
  document.documentElement.lang = lang;
  updatePageContent();
  localStorage.setItem("preferredLanguage", lang);

  // Update language switcher button states
  document.querySelectorAll(".lang-btn").forEach((btn) => {
    btn.classList.remove("active");
    if (btn.textContent.toLowerCase().includes(lang)) {
      btn.classList.add("active");
    }
  });
}

// Function to update all page content
function updatePageContent() {
  // Update all elements with data-i18n attribute
  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.getAttribute("data-i18n");
    const text = getText(key);

    if (element.tagName === "INPUT" || element.tagName === "TEXTAREA") {
      element.placeholder = text;
    } else {
      // Check if text contains HTML tags
      if (text.includes("<br>") || text.includes("<") || text.includes(">")) {
        element.innerHTML = text;
      } else {
        element.textContent = text;
      }
    }
  });

  // Update page title
  const titleKey = document.querySelector("title").getAttribute("data-i18n");
  if (titleKey) {
    document.title = getText(titleKey);
  }

  // Update meta description
  const metaDesc = document.querySelector('meta[name="description"]');
  if (metaDesc) {
    const descKey = metaDesc.getAttribute("data-i18n");
    if (descKey) {
      metaDesc.setAttribute("content", getText(descKey));
    }
  }

  // Update prices if on services page
  if (typeof injectPrices === "function") {
    injectPrices();
  }
}

// Initialize language on page load
document.addEventListener("DOMContentLoaded", function () {
  const savedLanguage = localStorage.getItem("preferredLanguage");
  if (savedLanguage && LOCALIZATION[savedLanguage]) {
    currentLanguage = savedLanguage;
  }

  document.documentElement.lang = currentLanguage;
  updatePageContent();

  // Set initial language switcher button state
  document.querySelectorAll(".lang-btn").forEach((btn) => {
    btn.classList.remove("active");
    if (btn.textContent.toLowerCase().includes(currentLanguage)) {
      btn.classList.add("active");
    }
  });
});

// Export for use in other scripts
if (typeof module !== "undefined" && module.exports) {
  module.exports = { LOCALIZATION, getText, changeLanguage, updatePageContent };
}
